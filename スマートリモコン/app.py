def _start_record(key: str):
    with _record_lock:
        if _record_status["state"] == "recording":
            return False
        _record_status["state"]   = "recording"
        _record_status["message"] = "リモコンのボタンを押してください..."

    def _run():
        cmd = ["python3", IRRP_PATH, "-r", f"-g{GPIO_RECV}", "-f", CODES_FILE,
               key, "--no-confirm", f"--post={POST_MS}"]
        try:
            proc = subprocess.run(cmd, capture_output=True, text=True, timeout=30)
            with _record_lock:
                if proc.returncode == 0:
                    _record_status["state"]   = "done"
                    _record_status["message"] = f"保存しました: {key}"
                else:
                    _record_status["state"]   = "error"
                    _record_status["message"] = proc.stderr or "recording failed"
        except subprocess.TimeoutExpired:
            with _record_lock:
                _record_status["state"]   = "error"
                _record_status["message"] = "タイムアウト — 信号を受信できませんでした"

    threading.Thread(target=_run, daemon=True).start()
    return True

