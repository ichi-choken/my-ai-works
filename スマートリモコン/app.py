def _carrier(gpio, frequency, micros):
    """irrp.py と同じ 38kHz キャリアバースト生成"""
    cycle = 1000.0 / frequency        # μs per cycle (38kHz → 26.3μs)
    cycles = int(round(micros / cycle))
    on  = int(round(cycle / 2.0))
    off = int(round(cycle - on))
    wf = []
    for _ in range(cycles):
        wf.append(pigpio.pulse(1 << gpio, 0,         on))
        wf.append(pigpio.pulse(0,         1 << gpio, off))
    return wf


def _send_ir_direct(key: str) -> tuple[bool, str]:
    """
    irrp.py と同じ方式（38kHz キャリアバースト）で IR 信号を送信する。

    Returns:
        (ok: bool, error_message: str)
    """
    import time
    codes = _read_codes()
    if key not in codes:
        return False, "code not found"

    pulses_us = codes[key]   # [mark, space, mark, space, ...] in microseconds
    if not pulses_us:
        return False, "empty pulse list"

    try:
        pi = pigpio.pi()
        if not pi.connected:
            return False, "pigpio not connected"

        pi.set_mode(GPIO_SEND, pigpio.OUTPUT)
        pi.wave_add_new()

        marks_wid  = {}
        spaces_wid = {}
        wave = []

        for i, duration in enumerate(pulses_us):
            if i % 2 == 0:
                # mark: 38kHz キャリアバースト
                if duration not in marks_wid:
                    wf = _carrier(GPIO_SEND, 38.0, duration)
                    pi.wave_add_generic(wf)
                    marks_wid[duration] = pi.wave_create()
                wave.append(marks_wid[duration])
            else:
                # space: 無音（GPIO LOW）
                if duration not in spaces_wid:
                    pi.wave_add_generic([pigpio.pulse(0, 0, duration)])
                    spaces_wid[duration] = pi.wave_create()
                wave.append(spaces_wid[duration])

        pi.wave_chain(wave)

        # 送信完了を待つ (最大5秒)
        deadline = time.time() + 5.0
        while pi.wave_tx_busy():
            if time.time() > deadline:
                pi.wave_tx_stop()
                pi.write(GPIO_SEND, 0)
                pi.stop()
                return False, "wave send timeout"
            time.sleep(0.01)

        # POST delay
        time.sleep(POST_MS / 1000.0)

        # wave リソース解放
        for wid in list(marks_wid.values()) + list(spaces_wid.values()):
            pi.wave_delete(wid)

        pi.write(GPIO_SEND, 0)  # GPIO を確実に LOW に戻す
        pi.stop()
        return True, ""

    except Exception as e:
        return False, str(e)


