AUTOMATIONS_FILE = os.path.join(os.path.dirname(__file__), "automations.json")

def _read_automations() -> list:
    if not os.path.exists(AUTOMATIONS_FILE):
        return []
    try:
        with open(AUTOMATIONS_FILE) as f:
            return json.load(f)
    except Exception:
        return []

def _write_automations(automations: list):
    with open(AUTOMATIONS_FILE, "w") as f:
        json.dump(automations, f, ensure_ascii=False, indent=2)

