    data   = request.get_json(force=True) or {}
    order  = data.get("order", [])
    codes  = _read_codes()
    prefix = f"{dev_id}:"
    # orderに含まれるキーだけ並び替え、残りはそのまま末尾
    existing = [k for k in codes if k.startswith(prefix)]
    full_order = [f"{dev_id}:{a}" for a in order if f"{dev_id}:{a}" in codes]
    rest = [k for k in existing if k not in full_order]
    new_codes = {k: v for k, v in codes.items() if not k.startswith(prefix)}
    for k in full_order + rest:
        new_codes[k] = codes[k]
    _write_codes(new_codes)
    return jsonify({"ok": True})


